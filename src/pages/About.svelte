<h1 class="text-4xl text-bold">QuickLaw - An App For All Legal Enquiries</h1>
<br />
<h2 class="text-3xl text-bold">Introduction</h2>

<p>
  In this day and age, everyone needs a lawyer. Whether it is to start a new
  business or to create bright futures. The process of law can be quite
  expensive and long.QUICK LAW! A solution to all of these problems. An app
  which guides an average person through their legal issues and helps you in
  finding an affordable lawyer.
</p>
<br />

<h2 class="text-3xl text-bold">Objective / Aim</h2>
<p>
  The app makes it easier for the common herd to find a good and affordable
  lawyer, you can send a PIL (A Public Interest Litigation is a petition that
  can be filed by any member of the public for any matter of public interest,
  for redress of public wrong or injury. ). You can search about your rights and
  fact check information .QuickLaw also helps you to research on law colleges of
  your choice.
</p>
<br />

<h2 class="text-3xl text-bold">Features Of The App</h2>

<p>
  The app comes with a spectacular feature of searching through a list of
  lawyers and has a search bar in which one can type in their legal issues or
  law related queries. The user can sort the lawyers through various filters
  like fees,location and experience. The app also provides a unique feature of
  instructing you on how to file a PIL.
</p>
